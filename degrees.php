        <h1>DEGREES &amp; CERTIFICATES</h1>

        <p>Las Positas College is authorized by the Board of Governors of the California Community Colleges to confer the Associate in Arts Degree, Associate in Science Degree, and Certificate of Achievement upon students who complete the necessary requirements.</p>

        <h2>Associate Degrees</h2>

        <p>The Associate in Arts (AA) and Associate in Science (AS) Degrees offered by Las Positas College include Transfer Programs and Occupational Programs. Within each AA and AS Degree, there is a required general education component (see the Las Positas College General Education Pattern charts in this Catalog). General Education courses are grouped into several broad areas and are designed to cultivate intellect and imagination and to broaden awareness of the relationship and interdependence of ideas and disciplines.</p>

        <h2>Associate in Arts Degree</h2>

        <p>The Associate in Arts Degree is conferred upon those students who complete the minimum of 60 semester units with a grade-point average of 2.0 or better and meet the graduation requirements as set forth in the “Las Positas College General Education Pattern for Associate in Arts Degree” chart in this Catalog.</p>

        <h2>Associate in Science Degree</h2>

        <p>The Associate in Science Degree is conferred upon those students who complete the minimum of 60 semester units with a grade-point average of 2.0 or better and meet the graduation requirements as set forth in the “Las Positas College General Education Pattern for Associate in Science Degree” chart in this Catalog.</p>

        <h2>Transfer with an Associate Degree</h2>

        <p>Las Positas College provides the freshman and sophomore years of a four-year college or university program. Students intending to transfer to four-year colleges and universities may complete lower division general education requirements and major field preparatory courses at Las Positas College. Students may also earn an associate degree while preparing for transfer. Meet with a counselor to develop an individualized plan.</p>

        <h2>AA/AS Occupational Programs</h2>

        <p>The courses of study within these programs prepare for direct job entry, and can normally be completed in a two-year period by a full- time student (carrying at least 15 units per semester). For a list of AA/ AS Programs see the “Transfer, Degree and Certificate Programs” chart in this Catalog. Students may also develop with a counselor an Individual Occupational Major which must be approved by the Dean of Student Services.</p>

        <h2>Certificate of Achievement</h2>

        <p>The courses of study within these programs prepare for direct job entry. Many certificate programs can be completed in one year in daytime classes, while others are designed to be completed over a longer period in evening classes. Students are advised to consult a counselor for more information about time frames for completion of certificate programs. For a list of Certificate Programs see the “Transfer, Degree and Certificate Programs” chart in this Catalog. Students may also develop an Individual Occupational Major with a counselor, for approval by the Dean of Student Services.</p>

        <p>The Certificate of Achievement is awarded upon successful completion of required courses for the certificate with a grade point average of 2.0 or higher.</p>

        <p>A student must complete at least eighty percent (80%) of the required program at Las Positas College or Chabot College, including the last three (3) units. Grades earned in non-degree applicable courses (numbered 100-200) will not be used in calculating a student’s degree applicable grade point average. No courses below the English 1A requirement are degree applicable.</p>

        <h2>Career Certificate</h2>

        <p>These certificates are not transcripted. This group of courses provides industry-based professional development. Individual courses will appear on transcript. For a list of Career Programs see the “Transfer, Degree and Certificate Programs” chart in this Catalog. See a counselor for further information. Should submit a request through the Division Dean.</p>


